	/*
	 * We store the last key pressed by the player as the direction Pacman will head towards if the tile is valid.
	 */
	void FixedUpdate () {
		if (Input.GetKey(KeyCode.Z))
		{
			if (isValid(Vector3.forward)){
				nextDir = Vector3.forward;
			}
			else{
				nextDir = curDir;
			}
		}
		else if (Input.GetKey(KeyCode.Q))
		{	
				if (isValid(Vector3.left)){
					nextDir = Vector3.left;
				}
			else{
				nextDir = curDir;
			}
		}
		else if (Input.GetKey(KeyCode.S))
		{
				if (isValid(Vector3.back)){
					nextDir = Vector3.back;
				}
			else{
				nextDir = curDir;
			}
		}
		else if (Input.GetKey(KeyCode.D))
		{
			if (isValid(Vector3.right)){
				nextDir = Vector3.right;
				}
			else{
				nextDir = curDir;
			}
		}
		move ();
	}
	

